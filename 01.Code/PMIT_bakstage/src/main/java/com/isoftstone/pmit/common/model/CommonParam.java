package com.isoftstone.pmit.common.model;

public class CommonParam {
	
	private String bu;
	private String du;
	private String pdu;
	private String cu;
	private String area;
	
	public String getBu() {
		return bu;
	}
	public void setBu(String bu) {
		this.bu = bu;
	}
	public String getDu() {
		return du;
	}
	public void setDu(String du) {
		this.du = du;
	}
	public String getPdu() {
		return pdu;
	}
	public void setPdu(String pdu) {
		this.pdu = pdu;
	}
	public String getCu() {
		return cu;
	}
	public void setCu(String cu) {
		this.cu = cu;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	
}
